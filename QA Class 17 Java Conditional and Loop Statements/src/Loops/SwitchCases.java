package Loops;

public class SwitchCases {

	public static void main(String[] args) {
		
		char grade='F';
		
		switch(grade) {
			case 'A':
				System.out.println("First Class");
				break;
			case 'B':
				System.out.println("Second Class");
				break;
			case 'C':
				System.out.println("Third Class");
				break;
			default:
				System.out.println("invalid grade");
		}
			
	}

}
