package operators;

public class PrintOper {

	public static void main(String[] args) {
	int a=10;
	int b=50;
	String name="kevin";
	System.out.println(a+b+name);
	System.out.println(name+a+b);
	System.out.println(a+name+b);
	System.out.println((a+b)+name);
	System.out.println(name+(a+b));
	System.out.println((name+a)+b);
	
	

	}

}
